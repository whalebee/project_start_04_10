#pragma region intToBin
void intToBin(int target)
{
	int remainder = target % 2; 
	target /= 2;		 
	if (target >= 2)
	{
		// 재귀함수
		intToBin(target);			
	}
	else
	{
		printf("%d ", target); // remainder 들어가면 안되지 ~ 몫 한 번 찍고		
	}
	printf("%d ", remainder);	// 그 다음 나머지 쫘아아악				
}
